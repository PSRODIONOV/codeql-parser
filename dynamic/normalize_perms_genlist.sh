#!/bin/bash
# Генерация списка эталонных исполняемых файлов (Git Bash / MSYS2, Windows-сторона).
#
# ВАЖНО: запускать через Git Bash, НЕ через WSL. Git Bash определяет +x по
# содержимому файла (shebang/PE-сигнатура) и расширению — этот результат
# надёжен. WSL же при включённой metadata всё равно отдаёт permissive-
# результат для файлов, у которых ещё нет записи в EA (т.е. для ВСЕХ
# файлов эталонного дерева, если оно само не было прежде chmod'нуто через
# metadata-aware WSL) — поэтому список через WSL получится "все файлы +x".
#
# Использование:
#   bash normalize_perms_genlist.sh <эталонный-каталог> <выходной-файл>
set -e

REFERENCE="$1"
OUT="$2"

if [ -z "$REFERENCE" ] || [ -z "$OUT" ]; then
  echo "Использование: $0 <эталонный-каталог> <выходной-файл-списка>"
  exit 1
fi

( cd "$REFERENCE" && find . -type f -perm -u+x | sed 's|^\./||' ) > "$OUT"
echo "Найдено исполняемых файлов: $(wc -l < "$OUT") -> $OUT"
